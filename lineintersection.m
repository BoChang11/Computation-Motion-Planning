function out = lineintersection(v1,v2,v3,v4)
d = (v4(2)-v3(2))*(v2(1)-v1(1))-(v4(1)-v3(1))*(v2(2)-v1(2));
u = (v4(1)-v3(1))*(v1(2)-v3(2))-(v4(2)-v3(2))*(v1(1)-v3(1));
v = (v2(1)-v1(1))*(v1(2)-v3(2))-(v2(2)-v1(2))*(v1(1)-v3(1));
if d<0
        u=-u;
        v=-v;
        d=-d;
end
out = (0<=u<=d) && (0<=v<=d);

